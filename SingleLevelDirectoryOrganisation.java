class SingleLevelDirectoryOrganisation{
    int dirno;
    String filename;
    
    SingleLevelDirectoryOrganisation(int dno){
        this.dirno=dno;
    }
    void setdirectoryname(String name)
    {
        this.filename=name;
    }
    String getdirectoryname()
    {
        return this.filename;
    }
    
}

    


